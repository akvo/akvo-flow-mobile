package org.akvo.flow.presentation.settings.passcode;

import android.app.AlertDialog;
import android.app.Dialog;
import android.content.DialogInterface;
import android.os.Bundle;
import androidx.annotation.NonNull;
import androidx.fragment.app.DialogFragment;

import org.akvo.flow.R;

public class PassCodeErrorDialog extends DialogFragment {

    public static final String TAG = "PassCodeErrorDialog";

    public PassCodeErrorDialog() {
    }

    public static PassCodeErrorDialog newInstance() {
        return new PassCodeErrorDialog();
    }

    @NonNull
    @Override
    public Dialog onCreateDialog(Bundle savedInstanceState) {
        AlertDialog.Builder builder = new AlertDialog.Builder(getContext());
        builder.setTitle(R.string.authfailed);
        builder.setMessage(R.string.invalidpassword);
        builder.setPositiveButton(R.string.okbutton, new DialogInterface.OnClickListener() {
            @Override
            public void onClick(DialogInterface dialog, int which) {
                dialog.dismiss();
            }
        });
        return builder.create();
    }
}
